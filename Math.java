import java.util.*;
class Math
{
public static void main(String args[])
{
Scanner sc=new Scanner(System.in);
System.out.println("enter the values of a and b:");
int a=sc.nextInt();
int b=sc.nextInt();
System.out.println("addition of"+a+"+"+b+"is"+(a+b));
System.out.println("addition of"+a+"-"+b+"is"+(a-b));
System.out.println("addition of"+a+"*"+b+"is"+(a*b));
System.out.println("addition of"+a+"/"+b+"is"+(a/b));
System.out.println("addition of"+a+"%"+b+"is"+(a%b));
}
}

